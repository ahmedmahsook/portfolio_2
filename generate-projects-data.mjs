if (!existsSync(videoDir)) {
  console.error(`Error: Directory ${videoDir} does not exist.`);
  process.exit(1);
}

// Get all files starting with opt_ and ending with .mp4
const files = readdirSync(videoDir);
const optVideos = files
  .filter(file => file.startsWith('opt_') && extname(file).toLowerCase() === '.mp4')
  .sort(); // Sort to ensure consistent ordering

console.log(`Found ${optVideos.length} optimized videos:`, optVideos);

// Map to projects list
const projects = optVideos.map((videoFile, index) => {
  // Use templates sequentially, fallback to general generation if we run out of templates
  const template = templates[index % templates.length];
  const nameWithoutExt = basename(videoFile, extname(videoFile));
  
  return {
    id: index + 1,
    title: template.title,
    subtitle: template.subtitle,
    category: template.category,
    description: template.description,
    video: `/video/${videoFile}`,
    poster: `/video/thumb_${nameWithoutExt}.jpg`,
    tags: template.tags
  };
});

// Write to components/project-data.ts
const outputPath = resolve('components/project-data.ts');
const fileContent = `// Automatically generated by generate-projects-data.mjs. Do not edit manually.

export interface Project {
  id: number;
  title: string;
  subtitle: string;
  category: "Editing" | "Marketing" | "Motion";
  description: string;
  video: string;
  poster: string;
  tags: string[];
}

export const projects: Project[] = ${JSON.stringify(projects, null, 2)};
`;

writeFileSync(outputPath, fileContent, 'utf-8');
